const fs = require('fs');
const path = require('path');

const svgDir = path.join(__dirname, '..', 'temp', 'element-plus-icons', 'packages', 'svg');
const outputFile = path.join(__dirname, '..', 'assets', 'icons', 'element-plus-icons.svg');

const svgFiles = fs.readdirSync(svgDir).filter(f => f.endsWith('.svg'));

const symbols = svgFiles.map(file => {
  const name = path.basename(file, '.svg');
  const content = fs.readFileSync(path.join(svgDir, file), 'utf-8');

  // 提取 SVG 内容
  const match = content.match(/<svg[^>]*>([\s\S]*)<\/svg>/i);
  if (!match) return null;

  const svgInner = match[1];
  const svgAttrs = content.match(/<svg([^>]*)>/i)?.[1] || '';

  // 提取 viewBox
  const viewBoxMatch = svgAttrs.match(/viewBox="([^"]*)"/i);
  const viewBox = viewBoxMatch ? viewBoxMatch[1] : '0 0 1024 1024';

  return `  <symbol id="el-${name}" viewBox="${viewBox}">\n${svgInner}\n  </symbol>`;
}).filter(Boolean);

const sprite = `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by build script, DO NOT edit manually -->
<svg xmlns="http://www.w3.org/2000/svg" style="display:none;">
${symbols.join('\n')}
</svg>
`;

fs.writeFileSync(outputFile, sprite, 'utf-8');

console.log(`Generated ${outputFile} with ${symbols.length} icons`);
