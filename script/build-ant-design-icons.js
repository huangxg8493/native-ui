/**
 * 将 Ant Design Icons SVG 文件转换为 SVG Sprite 格式
 * outlined 图标 ID: ant-{name}
 * filled 图标 ID: ant-fill-{name}
 */

const fs = require('fs');
const path = require('path');

const SVG_DIR = path.join(__dirname, '../temp/ant-design-icons/packages/icons-svg/svg');
const OUTLINED_DIR = path.join(SVG_DIR, 'outlined');
const FILLED_DIR = path.join(SVG_DIR, 'filled');
const OUTPUT_FILE = path.join(__dirname, '../assets/icons/ant-design-icons.svg');

function readSvgFile(filePath) {
    const content = fs.readFileSync(filePath, 'utf-8');
    // 移除 XML 声明和冗余空白，保留 svg 标签内容
    return content
        .replace(/<\?xml[^?]*\?>/g, '')
        .replace(/<svg[^>]*>/g, '')
        .replace(/<\/svg>/g, '')
        .trim();
}

function createSymbol(name, svgContent, prefix) {
    const id = prefix + name.replace(/-/g, '_').replace(/\.svg$/, '');
    return `  <symbol id="${id}" viewBox="0 0 1024 1024">\n${svgContent}\n  </symbol>`;
}

function generateSprite() {
    const symbols = [];

    // 处理 outlined 图标 (ant-{name})
    const outlinedFiles = fs.readdirSync(OUTLINED_DIR).filter(f => f.endsWith('.svg'));
    for (const file of outlinedFiles) {
        const name = file.replace('.svg', '');
        const content = readSvgFile(path.join(OUTLINED_DIR, file));
        symbols.push(createSymbol(name, content, 'ant-'));
    }
    console.log(`处理 outlined 图标: ${outlinedFiles.length} 个`);

    // 处理 filled 图标 (ant-fill-{name})
    const filledFiles = fs.readdirSync(FILLED_DIR).filter(f => f.endsWith('.svg'));
    for (const file of filledFiles) {
        const name = file.replace('.svg', '');
        const content = readSvgFile(path.join(FILLED_DIR, file));
        symbols.push(createSymbol(name, content, 'ant-fill-'));
    }
    console.log(`处理 filled 图标: ${filledFiles.length} 个`);

    const sprite = `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by build script, DO NOT edit manually -->
<svg xmlns="http://www.w3.org/2000/svg" style="display:none;">
${symbols.join('\n')}
</svg>
`;

    // 确保输出目录存在
    const outDir = path.dirname(OUTPUT_FILE);
    if (!fs.existsSync(outDir)) {
        fs.mkdirSync(outDir, { recursive: true });
    }

    fs.writeFileSync(OUTPUT_FILE, sprite, 'utf-8');
    console.log(`\n生成 SVG Sprite: ${OUTPUT_FILE}`);
    console.log(`总图标数: ${symbols.length}`);
}

generateSprite();
